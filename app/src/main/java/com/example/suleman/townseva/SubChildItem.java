public class SubChildItem {

    private  String id;
    private String serviceChildName;
    private String rate;
    private String imgLink;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getServiceChildName() {
        return serviceChildName;
    }

    public void setServiceChildName(String serviceChildName) {
        this.serviceChildName = serviceChildName;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getImgLink() {
        return imgLink;
    }

    public void setImgLink(String imgLink) {
        this.imgLink = imgLink;
    }
}
